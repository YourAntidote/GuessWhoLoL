var carteContainer = document.getElementById("carteContainer");
let btn = document.querySelector("#btn");
let undo = document.querySelector("#undo");

for (var i = 0; i < 166; i++) {
    var carte = document.createElement("div");
    carte.className = "carte champion";
    var img = document.createElement("img");
    img.src = pictureUrls[i % pictureUrls.length];
    carte.appendChild(img);
    carteContainer.appendChild(carte);
    console.log("Il y a " + (i + 1) + " champions.")
}

const allCardsArr = document.querySelectorAll(".carte");

btn.addEventListener('click', function () {
    reset();
})

function reset() {
    allCardsArr.forEach(function(carte, i) {
        allCardsArr[i].classList.remove("invisible");
    }
)};

let lastDelete;

allCardsArr.forEach(function(carte, i) {

    carte.addEventListener('click', function () {
        allCardsArr[i].classList.add('invisible');
        lastDelete = allCardsArr[i];
    });
});

undo.addEventListener('click', function(){
    lastDelete.classList.remove('invisible');
})
